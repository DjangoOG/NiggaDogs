echo "   –ü–æ—Å–º–æ—Ç—Ä–µ—Ç—å –ª–æ–≥–∏: sudo journalctl -u $SERVICE_NAME -f"
echo "   –ü–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç—å: sudo systemctl restart $SERVICE_NAME"
echo "   –û—Å—Ç–∞–Ω–æ–≤–∏—Ç—å: sudo systemctl stop $SERVICE_NAME"
echo ""
echo "‚ö†Ô∏è –ù–µ –∑–∞–±—É–¥—å—Ç–µ –Ω–∞—Å—Ç—Ä–æ–∏—Ç—å .env —Ñ–∞–π–ª: nano $BOT_DIR/.env"
